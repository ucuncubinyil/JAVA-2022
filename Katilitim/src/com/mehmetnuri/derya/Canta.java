package com.mehmetnuri.derya;

public class Canta {

    private String rengi;
    private String markasi;
    private String gozSayisi;
    private boolean deriMi;

    public Canta() {

    }

    public String getRengi() {
        return rengi;
    }

    public void setRengi(String rengi) {
        this.rengi = rengi;
    }

    public String getMarkasi() {
        return markasi;
    }

    public void setMarkasi(String markasi) {
        this.markasi = markasi;
    }

    public String getGozSayisi() {
        return gozSayisi;
    }

    public void setGozSayisi(String gozSayisi) {
        this.gozSayisi = gozSayisi;
    }

    public boolean isDeriMi() {
        return deriMi;
    }

    public void setDeriMi(boolean deriMi) {
        this.deriMi = deriMi;
    }
}
